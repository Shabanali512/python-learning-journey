# Simple Math Operations Program

val1 = int(input("Enter the first value please: "))
val2 = int(input("Enter the second value please: "))

total = val1 + val2
difference = val1 - val2
product = val1 * val2

print("The sum of 2 values is:", total)
print("The difference of 2 values is:", difference)
print("The multiplication of 2 values is:", product)
